// find max of two numbers using function expression

const maxi = function (num1, num2) {
  return Math.max(num1, num2);
};
console.log(maxi(4, 5));
console.log(maxi(5, 4));

//  concatenate two strings

const concat = function (str1, str2) {
  return str1 + str2;
};

console.log(concat("hi ", "bro"));
